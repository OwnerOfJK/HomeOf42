#include "libftprintf.h"

void	ft_putdec(double n)
{
	if (n == -2147483648)
	{
		ft_putstr("-2147483648");
	}
	else
	{
		if (n < 0)
		{
			ft_putchar('-');
			n = -n;
		}
		if (n > 9)
		{
			ft_putnbr(n / 10);
			//ft_putnbr(n % 10);
		}
		if (n < 10)
		{
			ft_putchar(n + 48);
		}
	}
}
