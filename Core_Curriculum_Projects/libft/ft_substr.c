#include "libft.h"

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	char	*str;
	size_t	i;

	if (s == NULL)
		return (NULL);
	i = 0;
	if (ft_strlen_const(s) < start)
		return (ft_strdup(""));
	if (len > ft_strlen_const(s) - start)
		len = ft_strlen_const(s) - start;
	str = malloc(len + 1);
	if (str == NULL)
		return (NULL);
	while (s[start] != '\0' && i < len)
	{
		str[i] = s[start];
		start++;
		i++;
	}
	str[i] = '\0';
	return (str);
}
